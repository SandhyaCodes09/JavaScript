// unshift()-> Adds element to the beginning(Methods that modify original array)

// example:- 1
let arr = [2, 3];
arr.unshift(1);// [1, 2, 3]


// example:- 2
let fruits = ["banana"];
fruits.unshift("apple");// ["apple", "banana"]

// example:- 3
let letters = ["B", "C"];
letters.unshift("A");// ["A", "B", "C"]

