// shift()-> Removes last element(Methods that modify original array)
// example:- 1
let arr = [10, 20, 30];
arr.shift();// [20, 30]

// example:- 2
let fruits = ["apple", "banana"];
fruits.shift();// ["banana"]

// example:- 2

let letters = ["A", "B", "C"];
letters.shift();// ["B", "C"]


// example:- 2

let numbers = [1];
numbers.shift(); // []
